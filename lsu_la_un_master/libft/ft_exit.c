#include "libft.h"
#include <stdlib.h>
#include <unistd.h>

void	pexit(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	exit(1);
}
